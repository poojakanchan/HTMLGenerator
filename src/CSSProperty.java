/*
 * Bean class which stores property and value pair
 */

public class CSSProperty {

	private String name;
	private String value;
	public String getValue() {
		return value;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
}
